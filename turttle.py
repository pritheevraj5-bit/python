import turtle
turtle.setup(300,200)
window=turtle.Screen()
window.title("my first turttle program")
window.bgcolor("yellow")
the_turtle=turtle.getturtle()
the_turtle.hideturtle()
the_turtle.setposition(50,35)
the_turtle.setposition(25,50)
the_turtle.setposition(-50,-25)
the_turtle.setposition(0,0)

turtle.exitonclick()
